  wc = wc - frby2xfrsq;
  wd = (frby6xfrsq)-fractby6;
  wb = wb + oneminusfractby2;

  /* Calculate cos value */
  cosVal = (cosVal + (b * wb)) + ((c * wc) + (d * wd));

  /* Return the output value */
  return (cosVal);
